package me.pete.ocarinalibrary.jsonobject;

public class DiscAPIDataResponseJsonObject {
    private String ITEM = "";
    private String DMS = "";
    private String DMSPCT = "";
    private String PCODE = "";
    private int BONUS_QTY = 0;
    private String MULTIPLE = "";
    private int MIN_QTY = 0;
    private int MAX_QTY = 0;

    public String getITEM() {
        return ITEM;
    }

    public void setITEM(String ITEM) {
        this.ITEM = ITEM;
    }

    public String getDMS() {
        return DMS;
    }

    public void setDMS(String DMS) {
        this.DMS = DMS;
    }

    public String getDMSPCT() {
        return DMSPCT;
    }

    public void setDMSPCT(String DMSPCT) {
        this.DMSPCT = DMSPCT;
    }

    public String getPCODE() {
        return PCODE;
    }

    public void setPCODE(String PCODE) {
        this.PCODE = PCODE;
    }

    public int getBONUS_QTY() {
        return BONUS_QTY;
    }

    public void setBONUS_QTY(int BONUS_QTY) {
        this.BONUS_QTY = BONUS_QTY;
    }

    public String getMULTIPLE() {
        return MULTIPLE;
    }

    public void setMULTIPLE(String MULTIPLE) {
        this.MULTIPLE = MULTIPLE;
    }

    public int getMIN_QTY() {
        return MIN_QTY;
    }

    public void setMIN_QTY(int MIN_QTY) {
        this.MIN_QTY = MIN_QTY;
    }

    public int getMAX_QTY() {
        return MAX_QTY;
    }

    public void setMAX_QTY(int MAX_QTY) {
        this.MAX_QTY = MAX_QTY;
    }
}
